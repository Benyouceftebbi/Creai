  export function getZrEcomCentersByWilaya(wilayaName: string): ZrEcomCenter[] {
    // Normalize the wilaya name for case-insensitive comparison
    const normalizedWilayaName = wilayaName.toLowerCase().trim()
  
    // First try to match by the wilaya_name property
    const centers = zrEcomCenters.filter(
      (center) => center.wilaya_name && center.wilaya_name.toLowerCase().trim() === normalizedWilayaName,
    )
  
    if (centers.length > 0) {
      return centers
    }
  
    // If no matches, try to find the wilaya ID from the name
    const wilayaIdEntry = Object.entries(wilayaNames).find(
      ([_, name]) => name.toLowerCase().trim() === normalizedWilayaName,
    )
  
    if (wilayaIdEntry) {
      return getZrEcomCentersByWilayaId(Number.parseInt(wilayaIdEntry[0]))
    }
  
    return []
  }
  
  // Function to get a ZR/ECOM center by its key
  export function getZrEcomCenterByKey(key: string): ZrEcomCenter | undefined {
    return zrEcomCenters.find((center) => center.key === key)
  }
  
  // Function to check if a wilaya has ZR/ECOM centers
  export function hasZrEcomCenters(wilayaName: string): boolean {
    return getZrEcomCentersByWilaya(wilayaName).length > 0
  }
  
